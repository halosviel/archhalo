-- discord presence
require("cord").setup({
  usercmds = true,
  reconnect = {
    enabled = true,
    interval = 5000,
  },
})

-- lualine setup + transparency
require("lualine").setup({
  options = {
    theme = {
      normal = { c = { bg = "none" } },
      inactive = { c = { bg = "none" } },
    },
    section_separators = "",
    component_separators = "",
  },
})

-- auto-reveal file in explorer on open
local explorer_opened = false

vim.api.nvim_create_autocmd("User", {
  pattern = "SnacksDashboardClosed",
  callback = function()
    explorer_opened = false
  end,
})

vim.api.nvim_create_autocmd("BufReadPost", {
  callback = function()
    if vim.bo.buftype == "" and vim.bo.filetype ~= "snacks_dashboard" then
      local buf = vim.api.nvim_get_current_buf()
      if not explorer_opened then
        explorer_opened = true
        require("snacks").explorer({
          cwd = "~/",
          reveal = true,
          hidden = true,
          auto_close = false,
        })
      else
        vim.schedule(function()
          pcall(function()
            Snacks.explorer.reveal(buf)
          end)
        end)
      end
    end
  end,
})

